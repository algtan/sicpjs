// sin x = 3*sin(x/3) - 4*sin^3(x/3)

// to reduce the size of the argument of sin. (For purposes of this exercise an angle is
// considered "sufficiently small" if its magnitude is not greater than 0.1 radians.)
// These ideas are incorporated in the following funcitons:

function cube(x) {
    return x * x * x;
}

function p(x) {
    return 3 * x - 4 * cube(x);
}

function sine(angle) {
    return ! (abs(angle) > 0.1)
           ? angle
           : p(sine(angle / 3));
}

